char	*ft_strrchr(const char *s, int c)
{
	char *tmp = ((void *)0);
	while (*s != '\0')
	{
		if (c == *s)
			tmp = s;
		s++;
	}
	if (c == *s)
		tmp = s;
	return (tmp);
}